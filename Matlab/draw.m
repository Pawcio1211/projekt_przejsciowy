function draw(a_11,...
        j_11, j_12, j_13,...
  j_20, j_21, j_22, j_23,...
  j_30, j_31, j_32, j_33,...
  j_40, j_41, j_42, j_43,...
  j_50, j_51, j_52, j_53)

j_f_1 = [-45 j_11 j_12 j_13 ; 0 4 3 2;...
    0 a_11 0.7 0.4    ; -90  0   0  0 ];
j_f_2 = [j_20 j_21 j_22 j_23 ; 7 4 2 1.5;...
    1.5  0   0   0    ;  0   0   0  0 ];
j_f_3 = [j_30 j_31 j_32 j_33 ; 8 4 3 2;...
    0    0   0   0    ;  0   0   0  0 ];
j_f_4 = [j_40 j_41 j_42 j_43 ; 7.5 4 3 2;...
    -1.5 0   0   0    ;  0   0   0  0 ];
j_f_5 = [j_50 j_51 j_52 j_53 ; 6 3 2 2;...
    -3   0   0   0    ;  0   0   0  0 ];

f_k_1 = t_matrix(j_f_1);
f_k_2 = t_matrix(j_f_2);
f_k_3 = t_matrix(j_f_3);
f_k_4 = t_matrix(j_f_4);
f_k_5 = t_matrix(j_f_5);

Q_1 = xyz(f_k_1);
Q_2 = xyz(f_k_2);
Q_3 = xyz(f_k_3);
Q_4 = xyz(f_k_4);
Q_5 = xyz(f_k_5);

Q_12 = [Q_2(:,2)'; Q_1(:,3)'];
Q_23 = [Q_3(:,2)'; Q_2(:,2)'];
Q_34 = [Q_4(:,2)'; Q_3(:,2)']; 
Q_45 = [Q_5(:,2)'; Q_4(:,2)']; 

plot3(Q_12(:,1),Q_12(:,2),Q_12(:,3),...
      Q_23(:,1),Q_23(:,2),Q_23(:,3),... 
      Q_34(:,1),Q_34(:,2),Q_34(:,3),... 
      Q_45(:,1),Q_45(:,2),Q_45(:,3),... 
      Q_1(1,:),Q_1(2,:),Q_1(3,:),'-o',...
      Q_2(1,:),Q_2(2,:),Q_2(3,:),'-o',...
      Q_3(1,:),Q_3(2,:),Q_3(3,:),'-o',...
      Q_4(1,:),Q_4(2,:),Q_4(3,:),'-o',...
      Q_5(1,:),Q_5(2,:),Q_5(3,:),'-o',...
      'Color','red','LineWidth',2,'MarkerSize',6,...
  'MarkerFaceColor','black','MarkerEdgeColor','black')
axis([-10,10,-10,10,0,20]);
grid on;
xlabel('X');
ylabel('Y');
zlabel('Z');
set(gca,'FontSize',20)

end